#pragma mark - Table view data source

- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView
{
    return 1;
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section
{
    return [_array count];
}

- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath
{
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:@"DeviceOption"];
    if (nil == cell) {
        cell = [[UITableViewCell alloc] initWithStyle:UITableViewCellStyleSubtitle reuseIdentifier:@"DeviceOption"];
        [cell.contentView setBackgroundColor:[UIColor darkGrayColor]];
        [cell setSelectedBackgroundView:[[UIView alloc] initWithFrame:[cell frame]]];
        [cell.selectedBackgroundView setBackgroundColor:[UIColor grayColor]];
        [cell.textLabel setTextColor:[UIColor whiteColor]];
    }
    [cell.textLabel setText:[_array objectAtIndex:indexPath.row]];
    return cell;
}

- (void)tableView:(UITableView *)tableView didSelectRowAtIndexPath:(NSIndexPath *)indexPath
{
    [tableView deselectRowAtIndexPath:indexPath animated:YES];
    [self blockSelectedItem]([_array objectAtIndex:indexPath.row], [indexPath row]);
    [self dismissViewControllerAnimated:YES completion:nil];
}

